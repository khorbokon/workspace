package Task84;

public class Calc84
{

    public static int sum (int valA, int valB){
        return valA + valB;
    }

    public static int vid (int valA, int valB){
        return valA - valB;
    }

    public static int mno (int valA, int valB){
        return valA * valB;
    }

    public static float dil (float valA1, float valB1) {
        return valA1 / valB1;
    }

}
